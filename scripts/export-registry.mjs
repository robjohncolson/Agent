#!/usr/bin/env node
/**
 * export-registry.mjs — Export lesson-registry.json as a JS file loadable
 * by the AP Stats roadmap app via <script src="registry-data.js">.
 *
 * Usage:
 *   node scripts/export-registry.mjs
 *   node scripts/export-registry.mjs --out /path/to/registry-data.js
 *
 * Default output: ../school/follow-alongs/registry-data.js
 */

import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REGISTRY_PATH = join(__dirname, "..", "state", "lesson-registry.json");
const DEFAULT_OUT = join(__dirname, "..", "..", "school", "follow-alongs", "registry-data.js");

// Parse --out flag
let outPath = DEFAULT_OUT;
const outIdx = process.argv.indexOf("--out");
if (outIdx !== -1 && process.argv[outIdx + 1]) {
  outPath = process.argv[outIdx + 1];
}

const registry = JSON.parse(readFileSync(REGISTRY_PATH, "utf-8"));

// Filter to only entries that have at least one useful URL
const filtered = {};
for (const [key, entry] of Object.entries(registry)) {
  if (!entry.urls) continue;
  const { worksheet, drills, quiz, blooket, schoologyFolder, videos } = entry.urls;
  const hasUrls = worksheet || drills || quiz || blooket || schoologyFolder ||
    (Array.isArray(videos) && videos.length > 0);
  if (!hasUrls) continue;

  filtered[key] = {
    unit: entry.unit,
    lesson: entry.lesson,
    urls: entry.urls,
    status: entry.status || {},
  };
}

const count = Object.keys(filtered).length;
const timestamp = new Date().toISOString();

const js = `// Auto-generated by Agent/scripts/export-registry.mjs — do not edit
// Generated: ${timestamp}
// Lessons with data: ${count}
const LESSON_REGISTRY = ${JSON.stringify(filtered, null, 2)};
`;

writeFileSync(outPath, js, "utf-8");
console.log(`Exported ${count} lessons to ${outPath}`);
