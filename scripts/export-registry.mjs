#!/usr/bin/env node
/**
 * export-registry.mjs — Export lesson-registry.json as a JS file loadable
 * by the AP Stats roadmap app via <script src="lesson-registry-data.js">.
 *
 * Usage:
 *   node scripts/export-registry.mjs
 *   node scripts/export-registry.mjs --out /path/to/output.js
 *
 * Default output: WORKSHEET_REPO/lesson-registry-data.js
 */

import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { AGENT_ROOT, WORKSHEET_REPO } from "./lib/paths.mjs";

const REGISTRY_PATH = join(AGENT_ROOT, "state", "lesson-registry.json");
const DEFAULT_OUT = join(WORKSHEET_REPO, "lesson-registry-data.js");

const args = process.argv.slice(2);
const outIdx = args.indexOf("--out");
const outPath = outIdx !== -1 && args[outIdx + 1] ? args[outIdx + 1] : DEFAULT_OUT;

const registry = JSON.parse(readFileSync(REGISTRY_PATH, "utf8"));

const slim = {};
for (const [key, entry] of Object.entries(registry)) {
  slim[key] = {
    unit: entry.unit,
    lesson: entry.lesson,
    urls: entry.urls || {},
    status: entry.status || {},
  };
}

const js = "// Auto-generated by Agent/scripts/export-registry.mjs \u2014 do not edit\nconst LESSON_REGISTRY = " + JSON.stringify(slim, null, 2) + ";\n";
writeFileSync(outPath, js, "utf8");
console.log("Wrote " + Object.keys(slim).length + " entries to " + outPath);
